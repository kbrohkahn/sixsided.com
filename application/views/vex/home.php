<p>VEX is a collectable, tradable and playable card game with the theme of flags. No flag knowledge is required, but as you play the games, you will learn a lot about current and historical flags from around the world. Flag selections include national, sub national and international flags, as well as selected historical flags, ensigns and jacks. Just navigate around this screen to learn more about VEX.</p>
