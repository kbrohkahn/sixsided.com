		<p>VEX IV, Cantons and Prefectures, includes more Sub-national flags from around the world. The 14 countries represented are Argentina, Belgium, Ethiopia, Italy, Japan, Liberia, Netherlands, Papua New Guinea, Finland, Norway, Portugal, Switzerland, United Kingdom and the Netherlands Antilles. Included are all 47 Japanese prefectures, with their attractive bicolor flags including rare orange and purple, all Swiss Cantons, the flag of Gibraltar, and the very attractive flags of the provinces of Belgium and the Netherlands.</p>
		<p>The games included in VEX IV include Quick Rules to VEX, Flags Unfurled, a color matching game that only uses the colors of each flag card, Color Guard; an exciting sequencing game that uses the points and colors of each flag card; and Flag Date, a calendar building game using the dates on each card. See details in VEX V for the last two games, Color Corral, and Flag Tribute. As usual, all games are playable with a single deck of 60 cards, so play and enjoy!</p>
