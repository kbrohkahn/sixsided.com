		<p>VEX V, International Entities, includes selected international flags with Heads of State, Naval Jacks, Naval Ensigns, Multi-national Flags, New Independent or Non-Independent Nations and International Signal Flags. Here's where you'll find flags such as the Royal Standard of Scotland, the Rising Sun Ensign, and the Confederate States Jack. But there's more! You'll also get 25 flags of international organizations, 35 flags of unrepresented peoples, and 40 international signal flags, complete with meanings.</p>
		<p>The games included in VEX V are basic VEX, Flags Unfurled, Color Guard; and Flag Date, See VEX IV for details of these first three games. VEX V also includes Color Corral, a wild trading game, and Flag Tribute, a skills-knowledge game that seeks to understand tertiary attributes of the cards, and lastly, Tournament Rules to Golf. Both VEX IV and V also include 10 superb ideas for sorting your complete collection of VEX cards.</p>
