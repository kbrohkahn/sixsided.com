		<p>VEX I, Nations of the World, is the first set in the series and was originally published in 2001. VEX I includes a selection of international flags from six continents around the globe: North America, South America, Asia, Europe, Africa, and Oceania. Included are all the members of the United Nations. Some of the more colorful flags include the 6 color South Africa flag, the five spread colors of the Seychelles, and the Golden Arrow of Guyana.</p>
		<p>The games included in VEX I are basic VEX, like Rummy; 18 Flags of Golf, an introduction to the card system whereby players match attributes of the "hole" card using "stroke" cards, and taking as few strokes as necessary; Flagship, a Battleship-like clone; 21-Flag Salute, like Blackjack; Capture the Flag, VEX's card game version of an outdoor game by the same name; and Flagstone, whereby players form sets of cards based upon criteria that are met with each flag.</p>
